# Create a list of just the class names, preserving order
COCO_CLASSES: List[str] = [class_name for class_name, _ in COCO_CLASSES_WITH_COLORS]

# Create a dictionary mapping class names to their colors
CLASS_COLORS: dict = {
    class_name: color for class_name, color in COCO_CLASSES_WITH_COLORS
}


# Function to get color for a class
def get_class_color(class_name: str) -> Tuple[int, int, int]:
    return CLASS_COLORS.get(class_name, DEFAULT_COLOR)
